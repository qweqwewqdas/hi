#include "libft.h"

t_list	*ft_lstnew(int content)
{
	t_list	*node;

	node = (t_list *)malloc(sizeof(t_list));
	if (!node)
		return (NULL);
	node->content = content;
	node->rank = 1;
	node->next = NULL;
	node->prev = NULL;
	return (node);
}
